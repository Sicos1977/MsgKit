        #region PropertyNameToCanonical
        /// <summary>
        ///     Returns the canonical name of the give <paramref name="propertyName" />. When the property does not
        ///     exists then <c>null</c> is returned
        /// </summary>
        /// <param name="propertyName"></param>
        /// <returns></returns>
        internal static string PropertyNameToCanonical(string propertyName)
        {
            return Properties.ContainsKey(propertyName)
                ? Properties[propertyName]
                : null;
        }
        #endregion

        #region CanonicalToPropertyName
        /// <summary>
        ///     Returns the property name of the give <paramref name="canonicalName" />. When the property does not
        ///     exists then <c>null</c> is returned
        /// </summary>
        /// <remarks>
        ///     When more then one property is found then the UNICODE property is returned instead
        ///     of it's Non-UNICODE counter parts
        /// </remarks>
        /// <param name="canonicalName">The canonical name of the <see cref="PropertyTags"/></param>
        /// <returns></returns>
        internal static string CanonicalToPropertyName(string canonicalName)
        {
            var matches = Properties.Where(m => m.Value.StartsWith(canonicalName)).Select(m => m.Key);
            var enumerable = matches as string[] ?? matches.ToArray();
            return enumerable.Count() == 1 ? enumerable.First() : enumerable.First(m => m.EndsWith("_W"));
        }
        #endregion
